package main

import "fmt";

func main(){

	// number
	fmt.Println("ini adalah int", 1)
	fmt.Println("ini juga int", 10)

	// floating point
	fmt.Println("ini floating point", 1.5)

	// boolean
	fmt.Println("ini boolean", true)
	fmt.Println("ini boolean", false)

	// string
	fmt.Println("Farhan Yudha Pratama")
	fmt.Println(len("Farhan Yudha Pratama"))
	fmt.Println("Farhan Yudha Pratama"[0]) // jika begini balikannya adalah byte
	fmt.Println("Farhan Yudha Pratama"[1]) // jika begini balikannya adalah byte
}